package control_statment;

public class Switch_statment_for_days {

	public static void main(String[] args) {
		// Day -1 is Monday is good good for you 
		// Day -2 is Tuesday  is good good for you
		// Day -3 is Wednesday  is good good for you
		// Day -4 is  Tuesday is good good for you
		// Day -5 is Friday  is good good for you
		// Day -6 is Saturday is good good for you
		// Day -7 is Sunday is good good for you
		// plz enter the valid day in between 1 to 7
		
		
		int day=2;
		
		
		switch (day) {
		case 1: {System.out.println("Monday is good good for you");}
		break;
		
		case 2: {System.out.println("Tuesday  is good good for you");}
		break;
		
		case 3: {System.out.println("Wednesday  is good good for you");}
		break;
			
		case 4: {System.out.println("Tuesday is good good for you");}
		break;
			
		case 5: {System.out.println("Friday  is good good for you");}
		break;
		
		case 6: {System.out.println("Saturday is good good for you");}
		break;
		
		case 7: {System.out.println("Sunday is good good for you");}
		break;
		
		
		default: {System.out.println("plz enter the valid day in between 1 to 7");}
		
	}
	
		}
	
}

		
		
		
	


